#!/bin/bash

# Функция для проверки параметров
check_input() {
  if [ $# -ne 4 ]; then
    echo "Error: Вам нужно передать 4 параметра."
    exit 1
  fi

  if ! [[ $1 =~ ^[1-6]$ ]] || ! [[ $2 =~ ^[1-6]$ ]] || ! [[ $3 =~ ^[1-6]$ ]] || ! [[ $4 =~ ^[1-6]$ ]]; then
    echo "Error: Введите только числовые значения цветов (от 1 до 6)."
    exit 1
  fi

  if [ "$1" = "$2" ] || [ "$3" = "$4" ]; then
    echo "Error: Цвета шрифта и фона одного столбца не должны совпадать."
    exit 1
  fi
}
